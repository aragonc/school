}

/**
 * Generar JSON de pa칤ses
 */
function generateCountriesJSON() {
    $countries = getCountriesData();
    header('Content-Type: application/json; charset=utf-8');
    echo json_encode($countries, JSON_UNESCAPED_UNICODE | JSON_PRETTY_PRINT);
}

/**
 * Obtener HTML de selector de pa칤ses
 */
function getCountriesSelectHTML($selectedCode = '', $id = 'country', $name = 'country', $required = true) {
    $countries = getCountriesData();
    $requiredAttr = $required ? 'required' : '';

    $html = "<select class=\"form-control\" id=\"{$id}\" name=\"{$name}\" {$requiredAttr}>\n";
    $html .= "    <option value=\"\">-- Selecciona tu pa칤s --</option>\n";

    foreach ($countries as $country) {
        $selected = ($country['code'] === $selectedCode) ? 'selected' : '';
        $html .= "    <option value=\"{$country['code']}\" {$selected}>{$country['flag']} {$country['name']}</option>\n";
    }

    $html .= "</select>\n";

    return $html;
}

